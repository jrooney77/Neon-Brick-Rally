// Collision
function checkBrickCollisions() {
  for (let column = 0; column < bricks.length; column++) {
    for (let row = 0; row < bricks[column].length; row++) {
      const brick = bricks[column][row];

      // Skip bricks that have already been hit and hidden.
      if (!brick.visible) {
        continue;
      }

      const ballLeft = ballX - ballRadius;
      const ballRight = ballX + ballRadius;
      const ballTop = ballY - ballRadius;
      const ballBottom = ballY + ballRadius;
      const brickRight = brick.x + brick.width;
      const brickBottom = brick.y + brick.height;

      // Check whether the ball's circle overlaps this brick's rectangle.
      const isHittingBrick =
        ballRight > brick.x &&
        ballLeft < brickRight &&
        ballBottom > brick.y &&
        ballTop < brickBottom;

      if (isHittingBrick) {
        // Bounce the ball whenever it hits a brick.
        ballSpeedY = -ballSpeedY;
        playSound("brick");

        if (brick.hitsRemaining > 1) {
          // Two-hit bricks become normal one-hit bricks after the first hit.
          brick.hitsRemaining--;
          return;
        }

        // Hide one-hit bricks, add points, count one fewer brick, and show
        // small visual effects. These effects do not affect collision logic.
        brick.visible = false;
        score += brickScoreValue;
        bricksRemaining--;
        updateScoreDisplay();
        spawnBrickParticles(brick);
        spawnFloatingText(`+${brickScoreValue}`, brick.x + brick.width / 2, brick.y + brick.height / 2);
        maybeCreatePowerup(brick);

        // If every brick in this level is gone, move forward or finish the game.
        if (bricksRemaining === 0) {
          advanceToNextLevel();
        }

        // Stop after one brick hit so the bounce stays simple.
        return;
      }
    }
  }
}

function movePaddle() {
  // Move right, but stop when the paddle reaches the right edge.
  if (rightPressed && paddleX < canvas.width - paddleWidth) {
    paddleX = clampPaddleX(paddleX + paddleSpeed);
  } else if (leftPressed && paddleX > 0) {
    // Move left, but stop when the paddle reaches the left edge.
    paddleX = clampPaddleX(paddleX - paddleSpeed);
  }
}

function moveBall() {
  checkWallCollisions();

  if (!checkPaddleCollision() && ballY + ballSpeedY > canvas.height - ballRadius) {
    loseLife();
    return;
  }

  // Move the ball diagonally by changing both its x and y positions each frame.
  ballX += ballSpeedX;
  ballY += ballSpeedY;
}

// Powerups
function maybeCreatePowerup(brick) {
  // Even-numbered levels can drop +1 life powerups, capped at maxLives.
  if (level % 2 === 0 && lives < maxLives && Math.random() < extraLifeDropChance) {
    spawnPowerup("extraLife", brick);
    return;
  }

  // Any level can drop the wide-paddle powerup.
  if (Math.random() < widePaddleDropChance) {
    spawnPowerup("widePaddle", brick);
  }
}

function spawnPowerup(type, brick) {
  powerups.push({
    type: type,
    x: brick.x + brick.width / 2 - powerupSize / 2,
    y: brick.y + brick.height / 2,
    width: powerupSize,
    height: powerupSize
  });
}

function updatePowerups() {
  // Move powerups downward and check whether the paddle catches them.
  for (let index = powerups.length - 1; index >= 0; index--) {
    const powerup = powerups[index];
    powerup.y += powerupSpeed;

    if (isPowerupCaught(powerup)) {
      activatePowerup(powerup.type);
      powerups.splice(index, 1);
    } else if (powerup.y > canvas.height) {
      powerups.splice(index, 1);
    }
  }
}

function isPowerupCaught(powerup) {
  const powerupBottom = powerup.y + powerup.height;
  const powerupRight = powerup.x + powerup.width;
  const paddleRight = paddleX + paddleWidth;

  return (
    powerupBottom >= paddleY &&
    powerup.y <= paddleY + paddleHeight &&
    powerupRight >= paddleX &&
    powerup.x <= paddleRight
  );
}

function activatePowerup(type) {
  if (type === "extraLife") {
    activateExtraLife();
  } else {
    activateWidePaddle();
  }
}

function activateWidePaddle() {
  // Catching the powerup makes the paddle wider for a few seconds.
  paddleWidth = widePaddleWidth;
  paddlePowerupEndTime = Date.now() + powerupDuration;
  paddleX = clampPaddleX(paddleX);
}

function activateExtraLife() {
  // Extra-life powerups add one life, but never above the maximum.
  lives = Math.min(lives + 1, maxLives);
  updateLivesDisplay();
}

function updatePaddlePowerup() {
  // When the timer ends, return the paddle to its normal size.
  if (paddleWidth === widePaddleWidth && Date.now() >= paddlePowerupEndTime) {
    paddleWidth = normalPaddleWidth;
    paddleX = clampPaddleX(paddleX);
    paddlePowerupEndTime = 0;
  }
}

function clearPowerups() {
  powerups = [];
  paddleWidth = normalPaddleWidth;
  paddlePowerupEndTime = 0;
}

// Collision helpers
function checkWallCollisions() {
  const nextBallX = ballX + ballSpeedX;
  const nextBallY = ballY + ballSpeedY;

  // If the next ball position would hit a side wall, reverse the x speed.
  // Reversing x speed makes the ball bounce left or right.
  if (nextBallX > canvas.width - ballRadius || nextBallX < ballRadius) {
    ballSpeedX = -ballSpeedX;
  }

  // If the next ball position would hit the top wall, reverse the y speed.
  // Reversing y speed makes the ball bounce up or down.
  if (nextBallY < ballRadius) {
    ballSpeedY = -ballSpeedY;
  }
}

function checkPaddleCollision() {
  const nextBallY = ballY + ballSpeedY;
  const nextBallBottom = nextBallY + ballRadius;
  const isMovingDown = ballSpeedY > 0;
  const isOverPaddle = ballX > paddleX && ballX < paddleX + paddleWidth;
  const isTouchingPaddle = nextBallBottom >= paddleY && nextBallBottom <= paddleY + paddleHeight;

  // Only bounce from the paddle when the ball is moving downward and touches it.
  if (isMovingDown && isOverPaddle && isTouchingPaddle) {
    const paddleCenter = paddleX + paddleWidth / 2;
    const hitDistanceFromCenter = ballX - paddleCenter;

    // Divide by half the paddle width to get a simple number from about -1 to 1.
    // Left side hits make ballSpeedX negative, right side hits make it positive.
    const hitPercent = hitDistanceFromCenter / (paddleWidth / 2);
    ballSpeedX = hitPercent * 4;

    // Always send the ball upward after it hits the paddle.
    ballSpeedY = -Math.abs(ballSpeedY);
    playSound("paddle");
    return true;
  }

  return false;
}

// Levels
function resetBall() {
  const levelSpeed = getBallSpeedForLevel(level);

  ballX = ballStartX;
  ballY = ballStartY;
  ballSpeedX = levelSpeed;
  ballSpeedY = -levelSpeed;
}

function getBallSpeedForLevel(levelNumber) {
  // Increase speed every few levels, but cap it so the game stays playable.
  const speedIncrease = Math.floor((levelNumber - 1) / 3) * 0.35;
  return Math.min(ballStartSpeedX + speedIncrease, maxBallSpeed);
}

function resetBallAndPaddle() {
  // Put the ball and paddle back in their starting positions after a missed ball.
  resetBall();
  paddleX = (canvas.width - paddleWidth) / 2;
}

function loseLife() {
  // The player loses one life when the ball falls below the paddle.
  lives--;
  updateLivesDisplay();
  playSound("loseLife");

  if (lives === 0) {
    // With no lives left, stop gameplay and show the game over message.
    gameState = "gameOver";
    playSound("gameOver");
  } else {
    // If lives remain, reset the ball and paddle so the player can continue.
    resetBallAndPaddle();
  }
}

function resetGame() {
  // Restarting returns every important game value to its starting state.
  score = 0;
  updateScoreDisplay();
  lives = 3;
  updateLivesDisplay();
  level = 1;
  updateLevelDisplay();
  gameState = "playing";
  clearPowerups();
  clearEffects();
  createLevel(level);
  resetBallAndPaddle();
  rightPressed = false;
  leftPressed = false;
}

function createLevel(levelNumber) {
  // A level is generated from the level number instead of hand-coding 50 screens.
  const layoutType = getLayoutType(levelNumber);
  bricks = createBrickGrid(layoutType, levelNumber);
  bricksRemaining = countVisibleBricks();
  updateLevelDisplay();
}

function getLayoutType(levelNumber) {
  // Rotate through 10 reusable layout patterns:
  // level 1 full grid, level 2 checkerboard, ... level 10 cross, then repeat harder.
  const layoutIndex = (levelNumber - 1) % layoutTypes.length;
  return layoutTypes[layoutIndex];
}

function createBrickGrid(layoutType, levelNumber) {
  const rowCount = getRowCountForLevel(levelNumber);
  const newBricks = [];

  for (let column = 0; column < brickColumns; column++) {
    newBricks[column] = [];

    for (let row = 0; row < rowCount; row++) {
      const visible = isBrickVisibleForLayout(layoutType, row, column, rowCount, brickColumns, levelNumber);

      newBricks[column][row] = {
        x: column * (brickWidth + brickPadding) + brickOffsetLeft,
        y: row * (brickHeight + brickPadding) + brickOffsetTop,
        width: brickWidth,
        height: brickHeight,
        hitsRemaining: visible ? getBrickHitCount(row, column, levelNumber) : 1,
        color: getBrickColor(row),
        visible: visible
      };
    }
  }

  return ensureMinimumVisibleBricks(newBricks);
}

function getRowCountForLevel(levelNumber) {
  // Later levels add a couple of rows, but the bricks still stay above the paddle.
  return Math.min(baseBrickRows + Math.floor((levelNumber - 1) / 20), maxBrickRows);
}

function isBrickVisibleForLayout(layoutType, row, column, rowCount, columnCount, levelNumber) {
  const centerColumn = Math.floor(columnCount / 2);
  const centerRow = Math.floor(rowCount / 2);

  if (layoutType === "fullGrid") {
    return true;
  }

  if (layoutType === "checkerboard") {
    return (row + column) % 2 === 0;
  }

  if (layoutType === "pyramid") {
    return Math.abs(column - centerColumn) <= row;
  }

  if (layoutType === "diamond") {
    return Math.abs(row - centerRow) + Math.abs(column - centerColumn) <= centerRow;
  }

  if (layoutType === "border") {
    return row === 0 || row === rowCount - 1 || column === 0 || column === columnCount - 1;
  }

  if (layoutType === "columns") {
    return column % 2 === 0;
  }

  if (layoutType === "stripes") {
    return row % 2 === 0;
  }

  if (layoutType === "staggered") {
    return (column + row % 2) % 3 !== 0;
  }

  if (layoutType === "randomGaps") {
    return getDeterministicRandom(levelNumber, row, column) > 0.25;
  }

  if (layoutType === "cross") {
    return row === centerRow || column === centerColumn;
  }

  return true;
}

function getDeterministicRandom(levelNumber, row, column) {
  // This makes random-gap levels repeatable for a given level number.
  const value = Math.sin(levelNumber * 100 + row * 10 + column) * 10000;
  return value - Math.floor(value);
}

function getBrickHitCount(row, column, levelNumber) {
  // Later levels introduce more two-hit bricks. These draw with dark borders.
  if (levelNumber < 8) {
    return 1;
  }

  const difficultyBand = Math.floor((levelNumber - 1) / 10);
  const toughPattern = (row + column + levelNumber) % Math.max(5 - difficultyBand, 2);
  return toughPattern === 0 ? 2 : 1;
}

function ensureMinimumVisibleBricks(newBricks) {
  let visibleCount = countVisibleCells(newBricks);

  if (visibleCount >= minVisibleBricks) {
    return newBricks;
  }

  for (let column = 0; column < newBricks.length; column++) {
    for (let row = 0; row < newBricks[column].length; row++) {
      if (!newBricks[column][row].visible) {
        newBricks[column][row].visible = true;
        visibleCount++;
      }

      if (visibleCount >= minVisibleBricks) {
        return newBricks;
      }
    }
  }

  return newBricks;
}

function countVisibleCells(brickGrid) {
  let visibleCount = 0;

  for (let column = 0; column < brickGrid.length; column++) {
    for (let row = 0; row < brickGrid[column].length; row++) {
      if (brickGrid[column][row] && brickGrid[column][row].visible) {
        visibleCount++;
      }
    }
  }

  return visibleCount;
}

function countVisibleBricks() {
  return countVisibleCells(bricks);
}

function advanceToNextLevel() {
  // After level 50, stop the game on the final win screen.
  if (level === maxLevel) {
    clearPowerups();
    gameState = "win";
    playSound("win");
    return;
  }

  // Otherwise prepare the next level, then pause briefly before play resumes.
  level++;
  updateLevelDisplay();
  clearPowerups();
  createLevel(level);
  resetBallAndPaddle();
  startLevelCountdown();
}

function startLevelCountdown() {
  // The countdown gives the player a short break before the next level starts.
  gameState = "levelTransition";
  levelCountdownEndTime = Date.now() + levelDelayDuration;
}

function updateLevelCountdown() {
  if (Date.now() >= levelCountdownEndTime) {
    gameState = "playing";
  }
}

// Game Loop
function gameLoop() {
  const currentFrameTime = performance.now();
  const deltaTime = Math.min(currentFrameTime - lastFrameTime, 40);
  lastFrameTime = currentFrameTime;

  // Clear the canvas before drawing the next frame.
  ctx.clearRect(0, 0, canvas.width, canvas.height);

  drawBricks();
  drawBall();
  drawPowerups();
  drawParticles();
  drawFloatingTexts();

  if (gameState !== "paused") {
    updatePaddlePowerup();
    updateParticles(deltaTime);
    updateFloatingTexts(deltaTime);
    movePaddle();
  }

  drawPaddle();
  drawScore();
  drawLevel();
  drawLives();

  // Only update timers, collisions, powerups, and ball movement while playing.
  if (gameState === "playing") {
    checkBrickCollisions();

    if (gameState === "playing") {
      updatePowerups();
      moveBall();
    }
  }

  if (gameState === "start") {
    drawStartScreen();
  } else if (gameState === "levelTransition") {
    updateLevelCountdown();
    drawLevelCountdownScreen();
  } else if (gameState === "paused") {
    drawPausedScreen();
  } else if (gameState === "win") {
    drawWinScreen();
  } else if (gameState === "gameOver") {
    drawGameOverScreen();
  }

  updateActionControls();
  updatePauseControl();

  // Ask the browser to run this function again before the next repaint.
  requestAnimationFrame(gameLoop);
}

createLevel(level);
updateHighScoreDisplay();
lastFrameTime = performance.now();
requestAnimationFrame(gameLoop);
