function isWaitingForStartOrRestart() {
  return gameState === "start" || gameState === "gameOver" || gameState === "win";
}

function canMovePaddleWithPointer() {
  return gameState === "playing" || gameState === "levelTransition";
}

function togglePause() {
  // Pausing is only for active gameplay. It does not start from the opening
  // screen, and it does not restart after game over or winning.
  if (gameState === "playing") {
    pauseGame();
  } else if (gameState === "paused") {
    resumeGame();
  }
}

function pauseGame() {
  gameState = "paused";
  pauseStartedAt = Date.now();

  // Stop keyboard paddle movement immediately if an arrow key is held down.
  rightPressed = false;
  leftPressed = false;
}

function resumeGame() {
  const pausedDuration = Date.now() - pauseStartedAt;

  // Wide-paddle powerups use a Date.now() end time. Push that end time forward
  // so the remaining powerup time is unchanged by pausing.
  if (paddleWidth === widePaddleWidth && paddlePowerupEndTime > 0) {
    paddlePowerupEndTime += pausedDuration;
  }

  pauseStartedAt = 0;
  gameState = "playing";
}

function startOrRestartGame() {
  // Start from the opening screen. Restart only after game over or after winning.
  if (gameState === "start") {
    gameState = "playing";
  } else if (gameState === "gameOver" || gameState === "win") {
    resetGame();
  }
}

