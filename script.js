function movePaddleToCanvasX(clientX) {
  const canvasBounds = canvas.getBoundingClientRect();
  const scaleX = canvas.width / canvasBounds.width;
  const pointerX = (clientX - canvasBounds.left) * scaleX;

  // Center the paddle on the pointer and clamp it inside the canvas.
  paddleX = clampPaddleX(pointerX - paddleWidth / 2);
}

function clampPaddleX(nextPaddleX) {
  return Math.max(0, Math.min(nextPaddleX, canvas.width - paddleWidth));
}

function unlockAudio() {
  // Browsers usually require a user action before sounds can play.
  const context = getAudioContext();

  if (context && context.state === "suspended") {
    context.resume().catch(function () {
      // If the browser blocks audio, keep the game running without sound.
    });
  }
}

function getAudioContext() {
  const AudioContextClass = window.AudioContext || window.webkitAudioContext;

  // If Web Audio is not supported, return null and let the game continue silently.
  if (!AudioContextClass) {
    return null;
  }

  if (!audioContext) {
    try {
      audioContext = new AudioContextClass();
    } catch (error) {
      return null;
    }
  }

  return audioContext;
}

function playTone(frequency, duration, type = "square", volume = 0.05, delay = 0) {
  if (isMuted) {
    return;
  }

  const context = getAudioContext();

  // If audio is unavailable or still blocked, skip the sound without breaking gameplay.
  if (!context || context.state !== "running") {
    return;
  }

  const startTime = context.currentTime + delay;
  try {
    const oscillator = context.createOscillator();
    const gain = context.createGain();

    oscillator.type = type;
    oscillator.frequency.setValueAtTime(frequency, startTime);
    gain.gain.setValueAtTime(volume, startTime);
    gain.gain.exponentialRampToValueAtTime(0.001, startTime + duration);

    oscillator.connect(gain);
    gain.connect(context.destination);
    oscillator.start(startTime);
    oscillator.stop(startTime + duration);
  } catch (error) {
    // Audio errors should never stop the game.
  }
}

function playSound(soundName) {
  // Each sound is just one or more short tones.
  if (soundName === "paddle") {
    playTone(360, 0.08, "square", 0.04);
  } else if (soundName === "brick") {
    playTone(620, 0.07, "triangle", 0.05);
  } else if (soundName === "loseLife") {
    playTone(180, 0.18, "sawtooth", 0.05);
  } else if (soundName === "gameOver") {
    playTone(220, 0.14, "sawtooth", 0.05);
    playTone(140, 0.2, "sawtooth", 0.05, 0.12);
  } else if (soundName === "win") {
    playTone(523, 0.1, "triangle", 0.05);
    playTone(659, 0.1, "triangle", 0.05, 0.12);
    playTone(784, 0.16, "triangle", 0.05, 0.24);
  }
}

