function drawPowerups() {
  // Powerups are small falling squares. Different colors mean different effects.
  for (const powerup of powerups) {
    ctx.beginPath();
    ctx.rect(powerup.x, powerup.y, powerup.width, powerup.height);
    ctx.fillStyle = powerup.type === "extraLife" ? "#22d3ee" : "#c084fc";
    ctx.fill();
    ctx.strokeStyle = "#f5d0fe";
    ctx.lineWidth = 2;
    ctx.stroke();
    ctx.closePath();

    if (powerup.type === "extraLife") {
      ctx.font = "14px Arial";
      ctx.fillStyle = "#082f49";
      ctx.fillText("+1", powerup.x + 4, powerup.y + 17);
    }
  }
}

function drawBricks() {
  // Loop through every column and row in the brick grid.
  for (let column = 0; column < bricks.length; column++) {
    for (let row = 0; row < bricks[column].length; row++) {
      const brick = bricks[column][row];

      // Only draw bricks that are still visible.
      if (brick.visible) {
        ctx.beginPath();
        ctx.rect(brick.x, brick.y, brick.width, brick.height);
        ctx.fillStyle = brick.color;
        ctx.fill();

        // Bricks with 2 hits left get a dark border. After one hit, the border disappears.
        if (brick.hitsRemaining > 1) {
          ctx.lineWidth = 3;
          ctx.strokeStyle = "#020617";
          ctx.stroke();
        }

        ctx.closePath();
      }
    }
  }
}

